const add = (a, b) => {
  return a + b;
};

const subtract = (a, b) => {
  return b - a;
};

const divide = (a, b) => {
  return a / b;
};

const product = (a, b) => {
  return a * b;
};

module.exports = { add, subtract, divide, product };

